		#INCLUDE <./HEADER.ASM>
		#INCLUDE <./MACROS_CARGA_DATOS_SUBRUTINAS_T.asm>
		#INCLUDE <./MACROS_TIEMPOS_FIJOS.asm>
		;---------------------------------------------------------
		;				DEFINICION DE CONSTANTES
		;---------------------------------------------------------
N_TEEHT EQU	.35; NUMERO DE DIENTES RELUCTOR
INI_TURN EQU .2; NUMERO DE VUELTAS INICIALES


		
		;---------------------------------------------------------
		;			DEFINICIÆN DE ACCIONES O MNEMÆNICOS
		;				PARA LOS PINES DE SALIDA
		;---------------------------------------------------------
		#DEFINE 	PMS_C1_SIGNAL			GPIO,.0
		#DEFINE 	CKP_SIGNAL				GPIO,.2; HABILITADO PARA INT
		#DEFINE		DEBUGPIN				GPIO,.1
		#DEFINE		DEBUGPIN2				GPIO,.5	



		;---------------------------------------------------------
		;              DEFINICION DE REGISTROS (GPR)
		;---------------------------------------------------------
		; REGISTROS DEL 0X50 AL 0X58 RESERVADOS PARA SUBRUTINAS DE TIEMPO
		; REGISTROS DEL 0X40 AL 0X4D RESERVADOS PARA RESCATE Y RECUPERA

CKP_CONT 	EQU	0X20; CONTADOR DE PULSOS CKP INICIALES
N_REG_SAVED	EQU	0X21;


TMR1H_LAST	EQU	0X30
TMR1L_LAST	EQU	0X31

SYNC_CKP	EQU	0X33
PMS_CONT	EQU 0X34


;-------------------------------------------------------------------------------
;********************************** SETUP **************************************
;-------------------------------------------------------------------------------

		;---------------------------------------------------------
		;					  CONFIG I/O PINS
		;---------------------------------------------------------
		;B1
		BCF				TRISIO,.0 ;PMS_C1_SIGNAL COMO SALIDA
		BSF				TRISIO,.2 ;CKP_SIGNAL COMO ENTRADA
		BCF				TRISIO,.1
		BCF				TRISIO,.5
		BCF				STATUS,RP0			;B0

;-------------------------------------------------------------------------------
;********************************* FIRMWARE *************************************
;-------------------------------------------------------------------------------
		;---------------------------------------------------------
		; 					CONDICIONES INICIALES
		;---------------------------------------------------------
		;CONSTRUIR EL TOTAL DE PULSOS INICIALES
INIT_PULSES_CONT EQU N_TEEHT*INI_TURN; NOTA: INIT_PULSE_CONT <= 255
		; LIMPIA EL CONTADOR CKP_CONT:
		CLRF			CKP_CONT
		; LIMPIA EL NÖMERO DE REGISTROS GUARDADOS
		CLRF			N_REG_SAVED


		CLRF			TMR1H_LAST
		CLRF			TMR1L_LAST

		CLRF			PMS_CONT
		CLRF			0X3F

		MOVLW			.1
		MOVWF			SYNC_CKP
		;---------------------------------------------------------
		;			CONFIGURACION DE TIMER1 (SIN HABILITAR)
		;---------------------------------------------------------
		;BIT 0 (BIT DE ENCENDIDO):
		;	HABILITA TIMER1 								[1]
		;	DETIENE EL TIMMER1 								[0]

		;BIT 1 (BIT SELECTOR DE FUENTE DE RELOJ);
		; 	RELOJ EXTERNO PROVENIENTE DEL PIN T1OSO/T1CLK 	[1]
		;	RELOJ INTERNO (4Mhz) 							[0]

		;BIT 2 (BIT DE CONTROL DE SINCRONIZACIÆN PARA LA ENTRA-
		;DA DEL RELOJ EXTERNO)
		;	IF (BIT 1 == 1){
		;		ENTRADA DE RELOJ EXTERNA NO SINCRONIZADA 	[1]
		;		ENTRADA DE RELOJ EXTERNA SINCRONIZADA 		[0]
		;	}
		;	ELSE {
		;		ESTE BIT ES IGNORADO CUANDO TMR1 USA INT CLK.
		;	}


		; BIT 3 (BIT CONTROLADOR PARA HABILITAR EL OSCILADOR LP)
		; IF (INTOSC CON SALIDA EN CLKOUT ESTê ACTIVADA)
		;	LP OSCILADOR ES ACTIVADO PARA EL RELOJ TIMMER 1	[1]
		;	OSCILADOR LP ES APAGADO							[0]
		; ELSE
		; 	ESTE BIT ES IGNORADO


		; BIT 4-5 (BITS PARA LA SELECCION DEL PRESCALER)
			;11 = 1:8 Prescale Value
			;10 = 1:4 Prescale Value
			;01 = 1:2 Prescale Value
			;00 = 1:1 Prescale Value

		; BIT 6 (BIT PARA HABILITAR EL TIMER1 POR PIN DE ENTRADA)
		; IF TMR1ON (BIT 0) == 0
		;	ESTE BIT ES IGNORADO
		; ELSE
		;	TIMER 1 ESTê ENCENDENDO SI T1G PIN ES '0'		[1]
		;	TIMER 1 ESTê ENCENDIDO							[0]


		MOVLW			B'00000000'
		MOVWF			T1CON






		;---------------------------------------------------------
		;  CONFIGURACION DE GP2 PARA INTERRUPCION (SIN HABILITAR)
		;---------------------------------------------------------
		; LA INTERRUPCIÆN SERê DIPSTARA POR UN FLACO DE:
		; 	FLANCO DE SUBIDA 			OPTION_REG,INTEDG = [1]
		;	FLANCO DE BAJADA			OPTION_REG,INTEDG = [0]
		
		BSF				STATUS,RP0;		B1
		BSF				OPTION_REG,INTEDG
		BCF				STATUS,RP0;		B0




		;---------------------------------------------------------
		;    START MOTOR SIGNAL (DETECCIÆN DE GIROS INICIALES)
		;---------------------------------------------------------
INI_DETECT:
		;¢PULSO EN EL CKP?
		BTFSC			CKP_SIGNAL
		GOTO			$-1
		BTFSS			CKP_SIGNAL
		;	NO. SIGUE VERIFICANDO
		GOTO 			$-1
		;	SI.
		NOP
		NOP
		NOP
		;	INCREMENTA EN 1 CKP_CONT
		INCF			CKP_CONT,F	
		;	VERIFICA SI ¢YA SE ALCANZÆ LA CUOTA INICIAL DE PULSOS?
		MOVLW			INIT_PULSES_CONT
		XORWF			CKP_CONT,W
		BTFSS			STATUS,Z
		;	NO SE A ALCANZADO. REGRESA A DETECTAR PULSOS
		GOTO			INI_DETECT
		;	YA SE ALCANZÆ:


		;LIMPIAMOS TMR1
		CLRF			TMR1L
		CLRF			TMR1H

		BSF				STATUS,RP0		;B1

		;HABILITAMOS EL PERMISO DE INT POR OVERFLOW TMR1:
	
		; SE ACTIVA EL BIT DE INTERRUPCION POR DESBORDE TMR1
		BSF				PIE1,TMR1IE
		; SE ACTIVA EL PERMISO DE INTERRUPCIÆN POR PERIFERICOS
		BSF				INTCON,PEIE

		; SE ACTIVA EL PERMISO DE INTERRUPCIÆN GENERAL
		BSF				INTCON,GIE
		BCF				STATUS,RP0		;B0


		;ENCENDEMOS EL 	TMR1
		BSF				T1CON,TMR1ON


		;HABILITAMOS EL PERMISO DE INT POR GP2:
		BSF				INTCON,INTE


		;¢YA SE GUARDARON LOS PRIMEROS 35 VALORES?
FIRST_SAVED:
		MOVLW			.35
		XORWF			N_REG_SAVED,W
		BTFSS			STATUS,Z
		;NO. ESPERA A QUE SE GUARDEN:
		GOTO 			FIRST_SAVED
	
		CLRF			N_REG_SAVED
		NOP
		NOP
		GOTO 			$-2
			




RSI:	
		;¢SE DESBORDÆ EL TIMER1?
		BTFSS			PIR1,.0
		;NO. FUE UN PULSO EN CKP_SIGNAL
		GOTO			CKP_PULSE_DETECT
		;SI.
		
		BSF				PMS_C1_SIGNAL
		GOTO			$


		



CKP_PULSE_DETECT:
		;BCF				DEBUGPIN2
		;DETEN EL TIMER 1:
		BCF				T1CON,TMR1ON

		;AUMNENTA EN 1 EL CONTADOR DE LOS REGISTROS TMR1 (L & H) GUARDADOS:
		INCF			N_REG_SAVED,F

		BCF				DEBUGPIN
		MOVLW			.1
		XORWF			N_REG_SAVED,W
		BTFSC			STATUS,Z
		BSF				DEBUGPIN
		
		BCF				PMS_C1_SIGNAL


		
		; VERIFICA CUANTOS PULSOS FALTAN PARA PMS_C1 ON:
		DECFSZ			PMS_CONT,F
		GOTO			$ +2
CKP_ON:
		BSF				PMS_C1_SIGNAL

	


		;COMPARA EL VALOR ACTUAL DEL TIMER1 CON EL ÖLTIMO GUARDADO:
		;PRIMERO, BYTES ALTOS:
		MOVF			TMR1H,W;				W <- TME1H
		SUBWF			TMR1H_LAST,W; 		W <- TMR1H_LAST - TMR1H
		; EL RESULTADO FUE 0?
		BTFSC			STATUS,Z
		; SI. ENTONCES TMR1H = TMR1H_LAST; EVALUA LOS REGISTROS BAJOS
		GOTO			BYTES_LESS_TEST 	
		;NO. EVALUA SI ¢EL RESULTADO ES MENOR A 0 (STATUS,C=0)?
		BTFSS			STATUS,C
		;SI. ENTONCES TMR1H	> TMR1H_LAST; ACTUALIZA SYNC_PULSE
		GOTO			UPGRADE_SYNC
		;NO. POR TRICOTOMëA TMR1H_LAST > TMRH; NO ACTUALICE SYNC_PULSE
		GOTO			ALL_REGISTERS_TEST

BYTES_LESS_TEST:
		;BYTES BAJOS:
		MOVF			TMR1L,W;				W <- TME1L
		SUBWF			TMR1L_LAST,W; 		W <- TMR1L_LAST - TMR1L
		; EL RESULTADO FUE 0?
		BTFSC			STATUS,Z
		; SI. ENTONCES TMR1L = TMR1L_LAST; NO ACTUALICE SYNC_PULSE
		GOTO			ALL_REGISTERS_TEST
		; NO. EVALUA SI ¢EL RESULTADO ES MENOR A 0 (STATUS,C=0)?
		BTFSC			STATUS,C
		;NO. POR TRICOTOMëA TMR1L_LAST > TMRL; NO ACTUALICE SYNC_PULSE  
		GOTO			ALL_REGISTERS_TEST
		;SI. ENTONCES TMR1L	> TMR1L_LAST; ACTUALIZA SYNC_PULSE

UPGRADE_SYNC:
		MOVF			N_REG_SAVED,W
		MOVWF			SYNC_CKP
		BCF				DEBUGPIN2
		COMF			0X3F
		BTFSS			STATUS,Z
		BSF				DEBUGPIN2
	
		;ACTUALIZA A TMR1H_LAST Y TMR1L_LAST
		MOVF			TMR1H,W
		MOVWF			TMR1H_LAST
		MOVF			TMR1L,W
		MOVWF			TMR1L_LAST
		
		



ALL_REGISTERS_TEST:


		;LIMPIA LOS REGISTROS TMR1:
		CLRF			TMR1H
		CLRF			TMR1L
		


		
		;¢YA SE GUARDARON 35 REGISTROS TMR1 (L & H)?
		MOVLW			.35
		XORWF			N_REG_SAVED,W
		BTFSS			STATUS,Z
		;NO. PREPARA TODO PARA QUE SE SIGAN COMPARANDO Y GUARDANDO:
		GOTO			NEXT_TIMER
		;VERIFICA SI SYNC_CKP != 0:
		DECF			SYNC_CKP,W
		BTFSC			STATUS,Z
		INCF			SYNC_CKP,F
		;SI. DETERMINA EL NÖMERO DE PULSOS FALTANTES:
		DECF			SYNC_CKP,F
		MOVF			SYNC_CKP,W
		MOVWF			PMS_CONT
		;LIMPIA EL CONTRADOR DE REGISTROS GUARDADOS
		CLRF			N_REG_SAVED
		;LIMPIA TMR1H_LAST Y TMR1L_LAST
		CLRF			TMR1H_LAST
		CLRF			TMR1L_LAST

NEXT_TIMER:
		
		;ENCENDEMOS EL 	TMR1
		BSF				T1CON,TMR1ON
		;BSF				DEBUGPIN2

REGRESA:
		;BAJA LA BANDERA DE INTERRUPCION OVERFLOW TMR1:
		BCF				PIR1,.0
		;BAJA LA BANDERA DE LA INTERRUPCION GP2:
		BCF				INTCON,INTF
		#INCLUDE <./RECUPERA.ASM>	
		RETFIE









		#INCLUDE <./SUBRUTINAS_TIEMPO_V2.asm>
		END